    private void logbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_logbtnActionPerformed
     
        if ((txtIpass.getText().equals(""))||(usertxt.getText().equals(""))) 
         {
            JOptionPane.showMessageDialog( this, "Please enter passcode","Error", JOptionPane.ERROR_MESSAGE);
             return;
         }
            if("Doctor".equals(Roles.getSelectedItem())){
             Maindoctor mdr=new Maindoctor();
             mdr.DoctorName=usertxt.getText();
             mdr.DoctorID=txtIpass.getText();
             mdr.login_doctor(mdr.DoctorName,mdr.DoctorID);
            
            }
            
            else if("Manager".equals(Roles.getSelectedItem())){
             Managerclass mgr=new Managerclass();
             mgr.username=usertxt.getText();
             mgr.passcode=txtIpass.getText();
             mgr.login_user(mgr.username,mgr.passcode);
           }   
             
            else{
             Receptionist res=new Receptionist();
             res.username=usertxt.getText();
             res.passcode=txtIpass.getText();
             res.login_user(res.username,res.passcode);  
                
            }
                      
    }//GEN-LAST:event_logbtnActionPerformed
        

    
